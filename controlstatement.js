// Control statement
// 1) if else

let a=18
if (a>=18){
    console.log("can vote");
}
else {
    console.log("cannot vote");
}

// 2)switch case

let b=2
switch (b) {
    case 1:
        console.log("Good morning");
        break;
    case 2:
        console.log("Good Afternoon");
        break;
    case 1:
        console.log("Good Night");
        break;

    default:
        break;
}