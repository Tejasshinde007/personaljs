// Loops
// 1)for loop:

for (let i=1;i<=10;i++){
    console.log(i);
    
}

console.log("<------------------------------------------------------------------>");

// 2) while loop

a=0
while (a<10){
    console.log(a);
    a++;
}
console.log(a);

console.log("<------------------------------------------------------------->");

// 3)do while loop

b=0
do{
    console.log(b);
}while(b<0);